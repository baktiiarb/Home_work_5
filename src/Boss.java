public class Boss {
    private int healsBoss;
    private int damageBoss;
    private  String protectionTypeBoss;

    public int getHealsBoss() {
        return healsBoss;
    }

    public void setHealsBoss(int healsBoss) {
        this.healsBoss = healsBoss;
    }

    public int getDamageBoss() {
        return damageBoss;
    }

    public void setDamageBoss(int damageBoss) {
        this.damageBoss = damageBoss;
    }

    public String getProtectionTypeBoss() {
        return protectionTypeBoss;
    }

    public void setProtectionTypeBoss(String protectionTypeBoss) {
        this.protectionTypeBoss = protectionTypeBoss;
    }
}
